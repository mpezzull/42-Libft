#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char digit;

	digit = 0;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		digit = n % 10 + '0';
		write(fd, &digit, 1);
	}
	else
	{
		digit = n + '0';
		write(fd, &digit, 1);
	}
}
